#include <iostream>
using namespace std;

#include <iostream>
#include <eigen3/Eigen/Dense>
using Eigen::MatrixXd;
using Eigen::Matrix3d;
using Eigen::Vector3d;

template <typename Derived>
     Eigen::Matrix<typename Derived::Scalar, 3, 3> ypr2R(const Eigen::MatrixBase<Derived> &ypr)
    {
        typedef typename Derived::Scalar Scalar_t;

        Scalar_t y = ypr(0) / 180.0 * M_PI;
        Scalar_t p = ypr(1) / 180.0 * M_PI;
        Scalar_t r = ypr(2) / 180.0 * M_PI;

        Eigen::Matrix<Scalar_t, 3, 3> Rz;
        Rz << cos(y), -sin(y), 0,
            sin(y), cos(y), 0,
            0, 0, 1;

        Eigen::Matrix<Scalar_t, 3, 3> Ry;
        Ry << cos(p), 0., sin(p),
            0., 1., 0.,
            -sin(p), 0., cos(p);

        Eigen::Matrix<Scalar_t, 3, 3> Rx;
        Rx << 1., 0., 0.,
            0., cos(r), -sin(r),
            0., sin(r), cos(r);

        return Rz * Ry * Rx;
    }

static Eigen::Vector3d R2ypr(const Eigen::Matrix3d &R)
    {
        Eigen::Vector3d n = R.col(0);
        Eigen::Vector3d o = R.col(1);
        Eigen::Vector3d a = R.col(2);

        Eigen::Vector3d ypr(3);
        double y = atan2(n(1), n(0));
        double p = atan2(-n(2), n(0) * cos(y) + n(1) * sin(y));
        double r = atan2(a(0) * sin(y) - a(1) * cos(y), -o(0) * sin(y) + o(1) * cos(y));
        ypr(0) = y;
        ypr(1) = p;
        ypr(2) = r;

        return ypr / M_PI * 180.0;
    }


int main()
{
	//yaw, pitch ,roll in ENU frame (world frame)
	Eigen::Vector3d ypr{-72, -2.328406, 0.456945};
	Eigen::Vector3d v_w{1.95709896739573,	0.630905851721762,	0.090277725830674};

	//get body to world frame rotation
	Eigen::Matrix3d Rwb = ypr2R(ypr);

	//get world frame to body frame rotation
	Eigen::Matrix3d Rbw = Rwb.inverse();
	//velocity in ENU frame

	Eigen::Vector3d v_b = Rbw * v_w;
	cout<<"body velocity: "<<v_b<<endl;


}
